<h5>CD-00002: Proto French Election</h5>
<p> This analog dataset to the 2002 French Presidential Election Dataset was collected by Jean-Francois Laslier, Karine Van der Straeten and Michel Balinski.  It consists of 398 approval ballots and subjective ratings on a 20 point scale collected over potential candidates for the 2002 French Presidential election cast by students at Institut d’Etudes Politiques de Paris.</p> <p>This dataset preserves both the approval ballots and the subjective ratings of the candidates by each of the voters.  The Approvals are coded as either a 1.0 for approved or a 0.0 for not approved.  The subjective ratings are on 20 point scale where a score of -1.0 is when no input was provided (as compared to a rating of 0.0, the lowest possible).</p>


<h5>ED-00027: Proto French Election</h5>
<p> This analog dataset to the 2002 French Presidential Election Dataset was collected by Jean-Francois Laslier, Karine Van der Straeten and Michel Balinski.  It consists of 398 approval ballots collected over potential candidates for the 2002 French Presidential election cast by students at Institut d’Etudes Politiques de Paris.</p> <p>This dataset is interesting as its companion dataset <a href="/data/combinatorial/frenchrate/">CD-00002</a> has both the subjective evaluations of the candidates, along with the approvals.  This dataset only preserves the approval ballots cast by the students.  As the candidate set is the potential presidential candidates (and thus, not the exact set used in ED-00026), this is presented as a separate dataset.</p>

